    a3.success(f"✅ Presión entrega: {res['P_final_psia']:.1f} ≥ {presion_entrega_min_psia} psia") if not res['alerta_presion'] else a3.error(f"⚠️ Presión entrega: {res['P_final_psia']:.1f} < {presion_entrega_min_psia} psia")
    
